    public DefaultListModel obtenerList(){
        DefaultListModel demo = new DefaultListModel();
        ListModel limo = jList1.getModel();
        for (int i = 0; i < limo.getSize(); i++){
            demo.addElement(limo.getElementAt(i));
        }
        return demo;
    }
    
    
